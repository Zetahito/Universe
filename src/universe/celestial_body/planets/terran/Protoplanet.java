package universe.celestial_body.planets.terran;

/**
 * <h1>Protoplaneta</h1>
 * Též planetární embryo.
 * Stále se formuje tvar této planety akrecí a kolapsem pevných částic.
 * Vzniká v protoplanetárním disku obklopující formující se hvězdu.
 * Průměrná hmotnost protoplanet se pohybuje od 10 do 100 trilionů tun.
 * Průměr má v řádech tisíců km.
 * @author Jakub Štych
 * @version 19.4.2023
 */
public class Protoplanet extends Terran {

    /**
     * <h2>Konstruktor protoplanety</h2>
     */
    Protoplanet() {
        setType("Terran");
        setTerranPlanetType("Protoplanet");
    }
}
