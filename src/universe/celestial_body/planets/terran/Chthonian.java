package universe.celestial_body.planets.terran;

/**
 * <h1>Chtonijský svět</h1>
 * Holá kamenná planeta s neexistující atmosférou.
 * Povrch je pokryt krátery po meteorech.
 * Planeta bývala plynným obrem, ale její atmosféra byla postupně odbourána do volného vesmíru.
 * Toto odbourání probíhá kvůli těsné vzdálenosti mezi planetou a mateřskou hvězdou.
 * Z planeta tak zbyl pouze obnažený pozůstatek v podobě kovového jádra.
 * Život by se zde nedokázal vyvinout.
 * @author Jakub Štych
 * @version 12.5.2023
 */
public class Chthonian extends Terran {

    /**
     * <h2>Konstruktor chtonijského světa</h2>
     */
    public Chthonian() {
        setType("Terran");
        setTerranPlanetType("Chthonian");
    }
}
